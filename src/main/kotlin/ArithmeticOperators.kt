fun main() {
    val satu = 12
    val dua = 3
    var hasil: Int

    hasil = satu + dua
    println("$satu + $dua = $hasil")

    hasil = satu - dua
    println("$satu - $dua = $hasil")

    hasil = satu * dua
    println("$satu * $dua = $hasil")

    hasil = satu / dua
    println("$satu / $dua = $hasil")

    hasil = satu % dua
    println("$satu % $dua = $hasil")

    //selain yang di atas, kita juga bisa menggunakan operator aritmatika overloading
}