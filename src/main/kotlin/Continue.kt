fun main() {
    var j = 0
    while (j < 7){
        if (j == 4){
            j++
            continue
        }
        println(j)
        j++
    }

    //--------------------------------
    var a = 20
    while (a > 10){
        if (a == 15){
            a--
            continue
        }
        println(a)
        a--
    }

    //--------------------------------
    var u = 1
    while (u <= 10){
        if (u == 7){
            u++
            continue
        }
        println(u)
        u++
    }
}
