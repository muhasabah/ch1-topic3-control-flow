fun main() {
    var nilai = 10
    while (nilai >= 1){
        println("While loop $nilai")
        nilai--
    }

    //--------------------------------------------------------------
    //syarat pembuatan perulangan while do harus menggunakan (var)
    var nilai2 = 1
    while (nilai2 <= 5){
        println("Nilai ke $nilai2")
        nilai2++
    }
}